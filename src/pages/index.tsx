import { Recipe } from "@prisma/client";
import { type NextPage } from "next";
import Head from "next/head";
import Filters from "y/components/filters";
import Spinner from "y/components/spinner";
import { api } from "y/utils/api";

const Home: NextPage = () => {
  const { data, isLoading } = api.recipe.findTopRated.useQuery({});

  return (
    <>
      <Head>
        <title>Recipme</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <>
        <Filters />
        <div className="align-center flex flex-1 flex-col justify-center">
          {isLoading ? (
            <Spinner />
          ) : (
            data?.map((recipe) => (
              <RecipePreviewCard recipe={recipe} key={recipe.id} />
            ))
          )}
        </div>
      </>
    </>
  );
};

const RecipePreviewCard = ({ recipe }: { recipe: Recipe }) => {
  return (
    <div className="card glass w-96">
      <figure className="h-[250px]">
        <img src={recipe.image} alt={recipe.title} className="w-full" />
      </figure>
      <div className="card-body">
        <h2 className="card-title">{recipe.title}</h2>
        <p>{recipe.description}</p>
        <div className="card-actions justify-end">
          <button className="btn-primary btn">Open</button>
        </div>
      </div>
    </div>
  );
};

export default Home;
